# 常用模块：time，datetime，random，python提供的py文件。
# 什么是模块：本质就是，py文件，封装语句的最小单位
# 自定义模块：
# 实际上就是定义.py,也就是自己在py文件里面写的代码
# 是可执行语句
a = 1
print(a)
# 是函数定义
def aaa():
    print( 'hello,world')

# 实际上就是定义.py,其中可以包含：变量定义，可执行语句，for循环，函数定义等，他们统称模块的成员

#模块的运行方式：
# 脚本方式：直接用解释器运行，或者在pycharm等软件中运行
# 模块方式：被其他的模块导入。为导入它的模块提供资源（变量，函数定义，类定义）。
